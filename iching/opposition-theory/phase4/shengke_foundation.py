    w("| Edge | Src trig | Tgt trig | XOR | Hamming |")
    w("|------|----------|----------|-----|---------|")
    for p in shengke_results['sheng_pairs']:
        w(f"| {p['src_elem']}→{p['tgt_elem']} "
          f"| {TRIGRAM_NAMES[p['src_trig']]} ({fmt3(p['src_trig'])}) "
          f"| {TRIGRAM_NAMES[p['tgt_trig']]} ({fmt3(p['tgt_trig'])}) "
          f"| {fmt3(p['xor'])} | {p['hamming']} |")
    w("")
    sh = shengke_results['sheng_hamming_dist']
    sh_total = sum(sh.values())
    sh_mean = sum(d * sh.get(d, 0) for d in range(4)) / sh_total
    w(f"**生 Hamming distribution:** {dict(sorted(sh.items()))}")
    w(f"**生 mean Hamming:** {sh_mean:.4f}\n")

    # 2.3 克-relationship structure
    w("### 2.3 克 (Overcoming) Relationship Structure\n")
    w("All trigram pairs where element(src) overcomes element(tgt).\n")
    w("| Edge | Src trig | Tgt trig | XOR | Hamming |")
    w("|------|----------|----------|-----|---------|")
    for p in shengke_results['ke_pairs']:
        w(f"| {p['src_elem']}→{p['tgt_elem']} "
          f"| {TRIGRAM_NAMES[p['src_trig']]} ({fmt3(p['src_trig'])}) "
          f"| {TRIGRAM_NAMES[p['tgt_trig']]} ({fmt3(p['tgt_trig'])}) "
          f"| {fmt3(p['xor'])} | {p['hamming']} |")
    w("")
    kh = shengke_results['ke_hamming_dist']
    kh_total = sum(kh.values())
    kh_mean = sum(d * kh.get(d, 0) for d in range(4)) / kh_total
    w(f"**克 Hamming distribution:** {dict(sorted(kh.items()))}")
    w(f"**克 mean Hamming:** {kh_mean:.4f}\n")

    # 2.4 Full 8×8 matrix
    w("### 2.4 Full 8×8 Trigram Interaction Matrix\n")
    w("体 (rows) × 用 (columns). Cell = Hamming distance / relation initial.\n")
    rel_initials = {"比和": "H", "生体": "A", "克体": "X", "体生用": "D", "体克用": "F"}
    header = "|  体\\用  | " + " | ".join(f"{fmt3(b)}" for b in range(NUM_TRIG)) + " |"
    w(header)
    w("|--------|" + "|".join(["----"] * NUM_TRIG) + "|")
    mat = shengke_results['matrix']
    for a in range(NUM_TRIG):
        cells = []
        for b in range(NUM_TRIG):
            info = mat[(a, b)]
            cells.append(f"d{info['hamming']}{rel_initials[info['relation']]}")
        w(f"| {fmt3(a)} | " + " | ".join(cells) + " |")
    w("")
    w("Legend: d=Hamming distance, H=比和(harmonious), A=生体(auspicious), X=克体(harmful), D=体生用(draining), F=体克用(favorable)\n")

    # 2.5 Hamming profiles by relationship
    w("### 2.5 Hamming Distance by Five-Phase Relationship\n")
    w("Excluding self-pairs (a=b). How does each relationship distribute over Hamming distances?\n")
    rh = shengke_results['rel_hamming']
    w("| Relation | d=0 | d=1 | d=2 | d=3 | Total | Mean d |")
    w("|----------|-----|-----|-----|-----|-------|--------|")
    for rel in relations:
        if rel in rh:
            hcounts = rh[rel]
            cts = [hcounts.get(d, 0) for d in range(4)]
            t = sum(cts)
            mean = sum(d * cts[d] for d in range(4)) / t if t > 0 else 0
            w(f"| {rel} | {cts[0]} | {cts[1]} | {cts[2]} | {cts[3]} | {t} | {mean:.4f} |")
    w("")

    # Comparison
    w("**Key finding:** Do 生 and 克 differ systematically in Hamming profile?\n")
    w(f"- 生 (generation): mean d = {sh_mean:.4f}")
    w(f"- 克 (overcoming): mean d = {kh_mean:.4f}")
    if abs(sh_mean - kh_mean) < 0.1:
        w("- **Result:** No significant difference — both span similar Hamming distances.\n")
    else:
        w(f"- **Result:** Differ by {abs(sh_mean - kh_mean):.4f} — "
          f"{'生 is more distant' if sh_mean > kh_mean else '克 is more distant'}.\n")

    # ══════════════════════════════════════════════════════════════════════
    # Q6: Five-Phase Cycle as Combinatorial Object
    # ══════════════════════════════════════════════════════════════════════
    w("## Q6: Five-Phase Cycle as Combinatorial Object\n")

    n_assign = q6_data['n_assignments']
    trad_idx = q6_data['trad_idx']
    trad_scores = q6_data['trad_scores']
    all_sheng_h = q6_data['all_sheng_hamming']
    all_ke_h = q6_data['all_ke_hamming']
    all_sheng_comp = q6_data['all_sheng_comp']
    all_ke_comp = q6_data['all_ke_comp']
    all_intra_h = q6_data['all_intra_hamming']

    w(f"### Enumeration\n")
    w(f"Total valid surjections (8 trigrams → 5 elements, partition 2,2,2,1,1): **{n_assign}**\n")
    w(f"Traditional assignment found at index: {trad_idx}\n")

    w(f"### Traditional Assignment Scores\n")
    w(f"| Metric | 生 (generation) | 克 (overcoming) | Overall |")
    w(f"|--------|-----------------|-----------------|---------|")
    w(f"| Mean Hamming | {trad_scores['sheng_hamming']:.4f} | {trad_scores['ke_hamming']:.4f} | — |")
    w(f"| Complement fraction (d=3) | {trad_scores['sheng_comp_frac']:.4f} | {trad_scores['ke_comp_frac']:.4f} | — |")
    w(f"| Intra-element Hamming | — | — | {trad_scores['intra_hamming']:.4f} |")
    w(f"| Edge pair count | {trad_scores['sheng_n']} | {trad_scores['ke_n']} | — |")
    w("")

    # Ranking
    def percentile_rank(arr, val):
        arr = np.array(arr)
        return 100.0 * np.sum(arr <= val) / len(arr)

    sh_pct = percentile_rank(all_sheng_h, trad_scores['sheng_hamming'])
    kh_pct = percentile_rank(all_ke_h, trad_scores['ke_hamming'])
    sc_pct = percentile_rank(all_sheng_comp, trad_scores['sheng_comp_frac'])
    kc_pct = percentile_rank(all_ke_comp, trad_scores['ke_comp_frac'])
    ih_pct = percentile_rank(all_intra_h, trad_scores['intra_hamming'])

    def interp(pct):
        if pct < 5 or pct > 95: return 'extreme'
        if pct < 25 or pct > 75: return 'moderate'
        return 'average'

    w(f"### Ranking Among All {n_assign} Assignments\n")
    w(f"| Metric | Traditional value | Percentile | Interpretation |")
    w(f"|--------|-------------------|------------|----------------|")
    w(f"| 生 mean Hamming | {trad_scores['sheng_hamming']:.4f} | {sh_pct:.1f}% | {interp(sh_pct)} |")
    w(f"| 克 mean Hamming | {trad_scores['ke_hamming']:.4f} | {kh_pct:.1f}% | {interp(kh_pct)} |")
    w(f"| 生 complement frac | {trad_scores['sheng_comp_frac']:.4f} | {sc_pct:.1f}% | {interp(sc_pct)} |")
    w(f"| 克 complement frac | {trad_scores['ke_comp_frac']:.4f} | {kc_pct:.1f}% | {interp(kc_pct)} |")
    w(f"| Intra-element Hamming | {trad_scores['intra_hamming']:.4f} | {ih_pct:.1f}% | {interp(ih_pct)} |")
    w("")

    # Distribution summaries
    w(f"### Distribution Summaries\n")
    for label, arr, trad_val in [
        ("生 mean Hamming", all_sheng_h, trad_scores['sheng_hamming']),
        ("克 mean Hamming", all_ke_h, trad_scores['ke_hamming']),
        ("生 complement fraction", all_sheng_comp, trad_scores['sheng_comp_frac']),
        ("克 complement fraction", all_ke_comp, trad_scores['ke_comp_frac']),
        ("Intra-element mean Hamming", all_intra_h, trad_scores['intra_hamming']),
    ]:
        a = np.array(arr)
        w(f"**{label}:** min={a.min():.4f}, max={a.max():.4f}, "
          f"mean={a.mean():.4f}, std={a.std():.4f}, traditional={trad_val:.4f}")
    w("")

    # ASCII histogram for 生 mean Hamming
    w("### Histogram: 生 Mean Hamming Distance\n")
    w("```")
    a = np.array(all_sheng_h)
    bins = np.linspace(a.min() - 0.01, a.max() + 0.01, 21)
    hist, edges = np.histogram(a, bins=bins)
    max_bar = max(hist)
    for i in range(len(hist)):
        lo, hi = edges[i], edges[i + 1]
        bar_len = int(50 * hist[i] / max_bar) if max_bar > 0 else 0
        marker = " ◄ TRAD" if lo <= trad_scores['sheng_hamming'] < hi else ""
        w(f"  {lo:5.2f}-{hi:5.2f} | {'█' * bar_len} {hist[i]}{marker}")
    w("```\n")

    # ASCII histogram for 克 mean Hamming
    w("### Histogram: 克 Mean Hamming Distance\n")
    w("```")
    a = np.array(all_ke_h)
    bins = np.linspace(a.min() - 0.01, a.max() + 0.01, 21)
    hist, edges = np.histogram(a, bins=bins)
    max_bar = max(hist)
    for i in range(len(hist)):
        lo, hi = edges[i], edges[i + 1]
        bar_len = int(50 * hist[i] / max_bar) if max_bar > 0 else 0
        marker = " ◄ TRAD" if lo <= trad_scores['ke_hamming'] < hi else ""
        w(f"  {lo:5.2f}-{hi:5.2f} | {'█' * bar_len} {hist[i]}{marker}")
    w("```\n")

    # ══════════════════════════════════════════════════════════════════════
    # Structural Analysis
    # ══════════════════════════════════════════════════════════════════════
    w("## Structural Analysis\n")

    w("### Observation 1: 体/用 Hamming is EXACTLY the baseline (theorem)\n")
    w(f"The 体/用 Hamming distribution is identical to the baseline 8×8 distribution.")
    w("This is not empirical — it is a structural theorem: each hexagram contributes 3 copies")
    w("of (upper, lower) and 3 copies of (lower, upper), so over all 64 hexagrams, every ordered")
    w("trigram pair (a, b) appears exactly 6 times. The 体/用 decomposition samples ALL trigram")
    w("pairs uniformly. The five-phase evaluation layer operates on a perfectly representative")
    w("sample — no bias toward opposition or similarity.\n")

    w("### Observation 2: Intra-element Hamming structure\n")
    for e, d in intra.items():
        w(f"- {e}: {fmt3(d['a'])} ⊕ {fmt3(d['b'])} = {fmt3(d['xor'])}, d={d['hamming']}")
    w("")
    w("Metal and Earth pair trigrams at d=1 (minimal perturbation); Wood pairs at d=3 (complement).")
    w(f"Traditional intra-element mean Hamming = {trad_scores['intra_hamming']:.4f}.\n")

    w("### Observation 3: 生 vs 克 Hamming profiles\n")
    w(f"生 mean: {sh_mean:.4f}, 克 mean: {kh_mean:.4f}")
    w(f"Difference: {abs(sh_mean - kh_mean):.4f}.")
    if abs(sh_mean - kh_mean) > 0.1:
        w(f"生 edges connect more Hamming-distant trigrams than 克 edges.\n")
    else:
        w("Both generation and overcoming span similar Hamming distances.\n")

    w("### Observation 4: Traditional assignment ranking\n")
    w(f"Among {n_assign} valid assignments:")
    w(f"- 生 Hamming: percentile {sh_pct:.1f}%")
    w(f"- 克 Hamming: percentile {kh_pct:.1f}%")
    w(f"- 生 complement frac: percentile {sc_pct:.1f}%")
    w(f"- 克 complement frac: percentile {kc_pct:.1f}%")
    w(f"- Intra-element Hamming: percentile {ih_pct:.1f}%\n")

    pcts = [sh_pct, kh_pct, sc_pct, kc_pct, ih_pct]
    extreme = any(p < 5 or p > 95 for p in pcts)
    if extreme:
        w("The traditional assignment is **extreme** on at least one metric —")
        w("it is NOT a random choice from the space of valid assignments.\n")
    else:
        moderate = any(p < 25 or p > 75 for p in pcts)
        if moderate:
            w("The traditional assignment is **moderately unusual** on some metrics —")
            w("not random but not maximally extreme either.\n")
        else:
            w("The traditional assignment is **unremarkable** on all metrics —")
            w("consistent with being chosen by criteria unrelated to Hamming structure.\n")

    return '\n'.join(lines)


