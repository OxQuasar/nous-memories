"""
Phase 4: 生克 (Shēng-Kè) Foundation Data

Establishes infrastructure for the five-phase (五行) analysis and answers:
  Q1: 体/用 as scale bridge — how hexagram→trigram decomposition interacts with KW opposition
  Q2: 生克 in Hamming/mask terms — the combinatorial structure of the traditional mapping
  Q6: Five-phase cycle as combinatorial object — how special is the traditional assignment?

Encoding: L1 = bit 0 (bottom), L6 = bit 5 (top).
Trigrams are 3-bit integers with same bit ordering.
"""

from collections import Counter
from itertools import combinations, permutations
from pathlib import Path

import numpy as np

# ─── Constants ────────────────────────────────────────────────────────────────

N = 6
NUM_HEX = 1 << N        # 64
MASK_ALL = (1 << N) - 1  # 0b111111
NUM_TRIG = 8
MASK3 = 0b111

# ─── Trigram names ────────────────────────────────────────────────────────────

TRIGRAM_NAMES = {  # keyed by value where bit0=bottom line, bit2=top line
    0b000: "Kun ☷", 0b001: "Zhen ☳", 0b010: "Kan ☵", 0b011: "Dui ☱",
    0b100: "Gen ☶", 0b101: "Li ☲",   0b110: "Xun ☴", 0b111: "Qian ☰",
}

# ─── Five-phase (五行) system ─────────────────────────────────────────────────

ELEMENTS = ["Wood", "Fire", "Earth", "Metal", "Water"]
ELEMENT_ZH = {"Wood": "木", "Fire": "火", "Earth": "土", "Metal": "金", "Water": "水"}

# Trigram → Element (traditional mapping)
TRIGRAM_ELEMENT = {  # keyed by value where bit0=bottom line, bit2=top line
    0b111: "Metal", 0b011: "Metal",   # Qian ☰, Dui ☱
    0b101: "Fire",                     # Li ☲
    0b001: "Wood",  0b110: "Wood",     # Zhen ☳, Xun ☴
    0b010: "Water",                    # Kan ☵
    0b100: "Earth", 0b000: "Earth",    # Gen ☶, Kun ☷
}

# 生 (generation) cycle: Wood→Fire→Earth→Metal→Water→Wood
SHENG_CYCLE = ["Wood", "Fire", "Earth", "Metal", "Water"]
SHENG_EDGES = [(SHENG_CYCLE[i], SHENG_CYCLE[(i + 1) % 5]) for i in range(5)]

# 克 (overcoming) cycle: Wood→Earth→Water→Fire→Metal→Wood
KE_CYCLE = ["Wood", "Earth", "Water", "Fire", "Metal"]
KE_EDGES = [(KE_CYCLE[i], KE_CYCLE[(i + 1) % 5]) for i in range(5)]

# Precompute: element generates/overcomes which element
SHENG_MAP = {src: tgt for src, tgt in SHENG_EDGES}  # src generates tgt
KE_MAP = {src: tgt for src, tgt in KE_EDGES}        # src overcomes tgt

# Inverse maps for "who generates me" / "who overcomes me"
SHENG_INV = {tgt: src for src, tgt in SHENG_EDGES}  # tgt is generated by src
KE_INV = {tgt: src for src, tgt in KE_EDGES}        # tgt is overcome by src

# ─── Bit operations ──────────────────────────────────────────────────────────

def popcount(x):
    return bin(x).count('1')

def reverse6(x):
    r = 0
    for i in range(N):
        if x & (1 << i):
            r |= 1 << (N - 1 - i)
    return r

def fmt6(x):
    return format(x, '06b')

def fmt3(x):
    return format(x, '03b')

def bit(x, i):
    return (x >> i) & 1

# ─── Trigram extraction ──────────────────────────────────────────────────────

def lower_trigram(x):
    return x & MASK3

def upper_trigram(x):
    return (x >> 3) & MASK3

# ─── KW pairing ─────────────────────────────────────────────────────────────

def kw_partner(x):
    rev = reverse6(x)
    return x ^ MASK_ALL if rev == x else rev

def is_palindrome6(x):
    return reverse6(x) == x

def generate_kw_pairs():
    paired = set()
    pairs = []
    for a in range(NUM_HEX):
        if a in paired:
            continue
        b = kw_partner(a)
        ptype = "palindromic" if is_palindrome6(a) else "reversal"
        if a > b:
            a, b = b, a
        pairs.append((a, b, ptype))
        paired.add(a)
        paired.add(b)
    return pairs

# ─── Hexagram KW signature ──────────────────────────────────────────────────

def hex_kw_signature(x):
    """Return the 6-bit XOR mask and (o,m,i) signature for a hexagram's KW pair."""
    b = kw_partner(x)
    xor = x ^ b
    # For reversal pairs, xor is palindromic with signature (o,m,i)
    o = bit(xor, 0)
    m = bit(xor, 1)
    i = bit(xor, 2)
    return xor, (o, m, i)

# ─── Five-phase relationship ─────────────────────────────────────────────────

def five_phase_relation(ti_elem, yong_elem):
    """
    Evaluate the five-phase relationship from 体's perspective.
    Returns one of: 比和, 生体, 克体, 体生用, 体克用
    """
    if ti_elem == yong_elem:
        return "比和"
    if SHENG_MAP[yong_elem] == ti_elem:
        return "生体"      # 用 generates 体 → auspicious
    if KE_MAP[yong_elem] == ti_elem:
        return "克体"      # 用 overcomes 体 → harmful
    if SHENG_MAP[ti_elem] == yong_elem:
        return "体生用"    # 体 generates 用 → draining
    if KE_MAP[ti_elem] == yong_elem:
        return "体克用"    # 体 overcomes 用 → favorable
    raise ValueError(f"No relation found: {ti_elem} vs {yong_elem}")

# ─── Hamming distance (n=3) ─────────────────────────────────────────────────

def hamming3(a, b):
    return popcount(a ^ b)

# ─── Q1: 体/用 as Scale Bridge ──────────────────────────────────────────────

def compute_tiyong_states():
    """
    For every hexagram × moving line, compute 体/用 assignment and all derived quantities.
    Returns list of 384 state dicts.
    """
    states = []
    for h in range(NUM_HEX):
        lo, up = lower_trigram(h), upper_trigram(h)
        kw_xor, kw_sig = hex_kw_signature(h)

        for line in range(1, 7):  # 1-indexed moving line
            if line <= 3:
                # Moving in lower → lower=用, upper=体
                ti, yong = up, lo
            else:
                # Moving in upper → upper=用, lower=体
                ti, yong = lo, up

            ti_elem = TRIGRAM_ELEMENT[ti]
            yong_elem = TRIGRAM_ELEMENT[yong]
            relation = five_phase_relation(ti_elem, yong_elem)
            xor3 = ti ^ yong
            hd = popcount(xor3)

            states.append({
                'hex': h, 'line': line,
                'ti': ti, 'yong': yong,
                'ti_elem': ti_elem, 'yong_elem': yong_elem,
                'relation': relation,
                'xor3': xor3, 'hamming': hd,
                'kw_xor': kw_xor, 'kw_sig': kw_sig,
            })
    return states


def analyze_tiyong(states):
    """Compute summary statistics for Q1."""
    results = {}

    # Distribution of n=3 Hamming distances
    results['hamming_dist'] = Counter(s['hamming'] for s in states)

    # Distribution of n=3 XOR masks
    results['xor_dist'] = Counter(s['xor3'] for s in states)

    # Baseline: all 64 ordered trigram pairs (or 28 unordered non-self pairs)
    baseline_hamming = Counter()
    for a in range(NUM_TRIG):
        for b in range(NUM_TRIG):
            baseline_hamming[hamming3(a, b)] += 1
    results['baseline_hamming'] = baseline_hamming

    # Cross-tabulation: KW signature × Hamming distance
    sig_x_hamming = {}
    for s in states:
        key = (s['kw_sig'], s['hamming'])
        sig_x_hamming[key] = sig_x_hamming.get(key, 0) + 1
    results['sig_x_hamming'] = sig_x_hamming

    # Cross-tabulation: KW signature × five-phase relation
    sig_x_phase = {}
    for s in states:
        key = (s['kw_sig'], s['relation'])
        sig_x_phase[key] = sig_x_phase.get(key, 0) + 1
    results['sig_x_phase'] = sig_x_phase

    # KW partner complementarity analysis
    pairs = generate_kw_pairs()
    partner_map = {}
    for a, b, _ in pairs:
        partner_map[a] = b
        partner_map[b] = a

    # Index states by (hex, line)
    state_idx = {(s['hex'], s['line']): s for s in states}

    # For each KW pair × line position, compare relations
    complementarity = []
    for a, b, ptype in pairs:
        for line in range(1, 7):
            sa = state_idx[(a, line)]
            sb = state_idx[(b, line)]
            complementarity.append({
                'a': a, 'b': b, 'ptype': ptype, 'line': line,
                'rel_a': sa['relation'], 'rel_b': sb['relation'],
                'ti_a': sa['ti'], 'yong_a': sa['yong'],
                'ti_b': sb['ti'], 'yong_b': sb['yong'],
            })

    # Distribution of (rel_a, rel_b) pairs
    results['complementarity_dist'] = Counter(
        (c['rel_a'], c['rel_b']) for c in complementarity
    )
    results['complementarity'] = complementarity

    return results


# ─── Q2: 生克 in Hamming/Mask Terms ─────────────────────────────────────────

def analyze_shengke_structure():
    """Full analysis of the five-phase mapping in Hamming/mask terms."""
    results = {}

    # Element → trigrams
    elem_trigrams = {}
    for t, e in TRIGRAM_ELEMENT.items():
        elem_trigrams.setdefault(e, []).append(t)
    for e in elem_trigrams:
        elem_trigrams[e].sort()
    results['elem_trigrams'] = elem_trigrams

    # 1. Intra-element structure
    intra = {}
    for e, trigs in elem_trigrams.items():
        if len(trigs) == 2:
            a, b = trigs
            intra[e] = {'a': a, 'b': b, 'xor': a ^ b, 'hamming': hamming3(a, b)}
    results['intra'] = intra

    # 2. 生-relationship structure
    sheng_pairs = []
    for src, tgt in SHENG_EDGES:
        for ts in elem_trigrams[src]:
            for tt in elem_trigrams[tgt]:
                sheng_pairs.append({
                    'src_elem': src, 'tgt_elem': tgt,
                    'src_trig': ts, 'tgt_trig': tt,
                    'xor': ts ^ tt, 'hamming': hamming3(ts, tt),
                })
    results['sheng_pairs'] = sheng_pairs
    results['sheng_hamming_dist'] = Counter(p['hamming'] for p in sheng_pairs)

    # 3. 克-relationship structure
    ke_pairs = []
    for src, tgt in KE_EDGES:
        for ts in elem_trigrams[src]:
            for tt in elem_trigrams[tgt]:
                ke_pairs.append({
                    'src_elem': src, 'tgt_elem': tgt,
                    'src_trig': ts, 'tgt_trig': tt,
                    'xor': ts ^ tt, 'hamming': hamming3(ts, tt),
                })
    results['ke_pairs'] = ke_pairs
    results['ke_hamming_dist'] = Counter(p['hamming'] for p in ke_pairs)

    # 4. Full 8×8 trigram interaction matrix
    matrix = {}
    for a in range(NUM_TRIG):
        for b in range(NUM_TRIG):
            ea, eb = TRIGRAM_ELEMENT[a], TRIGRAM_ELEMENT[b]
            rel = five_phase_relation(ea, eb)  # a=体, b=用
            matrix[(a, b)] = {
                'xor': a ^ b, 'hamming': hamming3(a, b), 'relation': rel,
            }
    results['matrix'] = matrix

    # 5. Hamming profiles by relationship type
    rel_hamming = {}
    for (a, b), info in matrix.items():
        if a == b:
            continue  # skip self-pairs for comparison
        rel_hamming.setdefault(info['relation'], []).append(info['hamming'])
    results['rel_hamming'] = {r: Counter(hds) for r, hds in rel_hamming.items()}

    return results


# ─── Q6: Five-Phase Cycle as Combinatorial Object ───────────────────────────

def enumerate_assignments():
    """
    Enumerate all surjections from 8 trigrams → 5 elements with partition (2,2,2,1,1).
    
    Strategy: choose which 3 elements get 2 trigrams (C(5,3)=10 ways),
    then assign trigrams to the 5 distinguishable element slots.
    
    Total = C(5,3) × C(8,2) × C(6,2) × C(4,2) × C(2,1) × C(1,1)
          = 10 × 28 × 15 × 6 × 2 × 1
          = 50400
    """
    trigrams = list(range(NUM_TRIG))
    assignments = []

    # Choose which 3 of 5 elements get pairs (size 2)
    for pair_elems in combinations(range(5), 3):
        single_elems = [i for i in range(5) if i not in pair_elems]

        # Assign 2 trigrams to each pair-element, then 1 each to singletons.
        # Elements are distinguishable, so no deduplication of the three pair-groups.
        for t0 in combinations(trigrams, 2):
            remaining1 = [t for t in trigrams if t not in t0]
            for t1 in combinations(remaining1, 2):
                remaining2 = [t for t in remaining1 if t not in t1]
                for t2 in combinations(remaining2, 2):
                    remaining3 = [t for t in remaining2 if t not in t2]
                    # remaining3 has exactly 2 trigrams → assign to single_elems
                    for perm in permutations(remaining3):
                        mapping = {}
                        for t in t0:
                            mapping[t] = pair_elems[0]
                        for t in t1:
                            mapping[t] = pair_elems[1]
                        for t in t2:
                            mapping[t] = pair_elems[2]
                        for i, t in enumerate(perm):
                            mapping[t] = single_elems[i]
                        assignments.append(mapping)

    return assignments


def score_assignment(mapping):
    """
    Compute Hamming coherence and opposition preservation scores for a trigram→element assignment.
    
    The 生 and 克 cycles are fixed on abstract elements (0-4).
    The mapping tells us which trigrams belong to which element.
    """
    # Element → trigrams for this mapping
    elem_trigs = {}
    for t, e in mapping.items():
        elem_trigs.setdefault(e, []).append(t)

    # Abstract element cycles (indices into ELEMENTS)
    # 生: 0→1→2→3→4→0 (Wood→Fire→Earth→Metal→Water)
    # 克: 0→2→4→1→3→0 (Wood→Earth→Water→Fire→Metal)
    sheng_edges_idx = [(i, (i + 1) % 5) for i in range(5)]
    ke_edges_idx = [(0, 2), (2, 4), (4, 1), (1, 3), (3, 0)]

    def edge_stats(edges):
        """Compute mean Hamming and complement fraction for a set of directed edges."""
        total_hamming = 0
        total_pairs = 0
        complement_count = 0  # pairs at max Hamming distance (d=3)
        for src_e, tgt_e in edges:
            src_trigs = elem_trigs.get(src_e, [])
            tgt_trigs = elem_trigs.get(tgt_e, [])
            for ts in src_trigs:
                for tt in tgt_trigs:
                    h = hamming3(ts, tt)
                    total_hamming += h
                    total_pairs += 1
                    if h == 3:
                        complement_count += 1
        mean_h = total_hamming / total_pairs if total_pairs > 0 else 0
        comp_frac = complement_count / total_pairs if total_pairs > 0 else 0
        return mean_h, comp_frac, total_pairs

    # Intra-element coherence: mean Hamming within elements (lower = more coherent)
    intra_hamming = 0
    intra_count = 0
    for e_idx in range(5):
        trigs = elem_trigs.get(e_idx, [])
        for i in range(len(trigs)):
            for j in range(i + 1, len(trigs)):
                intra_hamming += hamming3(trigs[i], trigs[j])
                intra_count += 1
    intra_mean = intra_hamming / intra_count if intra_count > 0 else 0

    sheng_h, sheng_comp, sheng_n = edge_stats(sheng_edges_idx)
    ke_h, ke_comp, ke_n = edge_stats(ke_edges_idx)

    return {
        'sheng_hamming': sheng_h,
        'sheng_comp_frac': sheng_comp,
        'sheng_n': sheng_n,
        'ke_hamming': ke_h,
        'ke_comp_frac': ke_comp,
        'ke_n': ke_n,
        'intra_hamming': intra_mean,
    }


def find_traditional_index(assignments):
    """Find the index of the traditional mapping in the enumerated assignments."""
    # Traditional: element index mapping
    # 0=Wood, 1=Fire, 2=Earth, 3=Metal, 4=Water
    trad = {}
    for t, e_name in TRIGRAM_ELEMENT.items():
        trad[t] = ELEMENTS.index(e_name)

    for i, m in enumerate(assignments):
        if m == trad:
            return i
    return -1


